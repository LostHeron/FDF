#include <stdio.h>

void	print_mat(double mat[3][3])
{
	size_t	i;
	size_t	j;

	i = 0;
	while (i < 3)
	{
		j = 0;
		while (j < 3)
		{
			printf("%f, ", mat[i][j]);
			j++;
		}
		printf("\n");
		i++;
	}
	return ;
}
